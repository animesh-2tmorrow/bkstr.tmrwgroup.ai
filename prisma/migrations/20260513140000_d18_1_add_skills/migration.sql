-- Per docs/decisions.md D18.1 and docs/phase-6-roadmap.md (Stream L):
-- adds the `skills` / `skill_versions` / `skill_files` / `skill_prices` tables
-- (parallel to books / book_versions / book_chapters / book_prices) and
-- extends `access_grants` to polymorphically point at either a book or a
-- skill via a nullable `skill_id` column alongside the now-nullable `book_id`.
--
-- Per-row invariant ("exactly one of book_id/skill_id is non-null") is enforced
-- via a CHECK constraint added below. The prior @@unique([subscriberId,
-- bookId, source]) is dropped because NULL-distinct unique semantics in
-- Postgres would silently allow duplicate skill grants (or duplicate book
-- grants once skill_id rows mix in); two PARTIAL unique indexes replace it,
-- one per content type.
--
-- Body sections:
--   1. SQL generated by `npx prisma migrate diff` against the schema delta
--      (CreateEnum SkillStatus, AlterTable access_grants, CreateTable for
--      the four new skill tables, indexes, foreign keys).
--   2. Hand-authored block (D18.1 §1c) — partial unique indexes + XOR CHECK.
--
-- NO BACKFILL. New tables start empty. Existing access_grants rows all have
-- book_id non-null and skill_id null — the XOR CHECK holds against current
-- production data. The pre-deploy verification `SELECT count(*) FROM
-- access_grants WHERE book_id IS NULL` must return 0 (verified before merge).
--
-- Deploy model (D18.1 §0a): commits 1–5 land on `main` as a single
