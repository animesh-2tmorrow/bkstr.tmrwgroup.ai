-- (2) Hand-authored block — D18.1 §1c
--
-- Partial unique indexes (one per content type) replace the dropped combined
-- unique; XOR CHECK enforces "exactly one of book_id/skill_id non-null."
-- ═════════════════════════════════════════════════════════════════════════════

-- Partial unique indexes — one per content type
CREATE UNIQUE INDEX "access_grants_book_unique"
  ON "access_grants" ("subscriber_id", "book_id", "source")
  WHERE "book_id" IS NOT NULL;

CREATE UNIQUE INDEX "access_grants_skill_unique"
  ON "access_grants" ("subscriber_id", "skill_id", "source")
  WHERE "skill_id" IS NOT NULL;

-- XOR CHECK: exactly one of (book_id, skill_id) is non-null on every row.
-- Verified pre-deploy via `SELECT count(*) FROM access_grants WHERE
-- book_id IS NULL` (must equal 0 before this migration applies — all existing
-- grants are book grants).
ALTER TABLE "access_grants"
  ADD CONSTRAINT "access_grants_target_xor"
  CHECK (("book_id" IS NULL) <> ("skill_id" IS NULL));
